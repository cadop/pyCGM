import unittest
import pyCGM_Single.pyCGM as pyCGM

class TestCalculations(unittest.TestCase):

    def test_getangle_sho(self):
        axisP = [[0.0464229, 0.99648672, 0.06970743], [0.99734011, -0.04231089, -0.05935067], [-0.05619277, 0.07227725, -0.99580037]]
        axisD = [[-0.18067218, -0.98329158, -0.02225371], [0.71383942, -0.1155303, -0.69071415], [0.67660243, -0.1406784, 0.7227854]]
        self.assertEqual(pyCGM.getangle_sho(axisP,axisD), [-3.3474505645829722, -140.28662967555562, 172.50982144894826])

    def test_getangle_spi(self):
        axisP = [[0.0464229, 0.99648672, 0.06970743], [0.99734011, -0.04231089, -0.05935067], [-0.05619277, 0.07227725, -0.99580037]]
        axisD = [[-0.18067218, -0.98329158, -0.02225371], [ 0.71383942, -0.1155303, -0.69071415], [0.67660243, -0.1406784, 0.7227854]]
        self.assertEqual(pyCGM.getangle_spi(axisP,axisD), [-9.8208335778582327, -2.8188534655021193, -4.0537090802755111])

    def test_getangle(self):
        axisP = [[0.0464229, 0.99648672, 0.06970743], [0.99734011, -0.04231089, -0.05935067], [-0.05619277, 0.07227725, -0.99580037]]
        axisD = [[-0.18067218, -0.98329158, -0.02225371], [0.71383942, -0.1155303, -0.69071415],[0.67660243, -0.1406784, 0.7227854]]
        self.assertEqual(pyCGM.getangle(axisP,axisD), [37.141616013785963, -9.5416640443905497e-13, 89.999999999997684])


